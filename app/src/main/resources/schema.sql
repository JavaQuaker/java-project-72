DROP TABLE IF EXISTS url_checks;
DROP TABLE IF EXISTS urls;

CREATE TABLE urls (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT,
    created_at TIMESTAMP
);

CREATE TABLE url_checks (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
status_code INT,
h1 TEXT,
title TEXT,
description TEXT,
created_at TIMESTAMP,
url_id bigint REFERENCES urls(id)
);